package DAY7;

public class Star {
	public static void main(String args[]) {
		int m,j,i;
		for(m=1; m<=6; m++)
		{
		for(j=10; j>=m; j--)
		{
		System.out.print(" ");
		}
		for(i=1; i<=(2*m-1); i++)
		{
		System.out.print("*");
		}
		System.out.println("");
		
		
		
			
	}
}}
